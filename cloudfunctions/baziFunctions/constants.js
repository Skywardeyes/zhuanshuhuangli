// ── 十神 ──
function getShishen(dayGan, otherGan) {
  const dayWx = GAN_WUXING[dayGan];
  const otherWx = GAN_WUXING[otherGan];
  const dayYy = GAN_YIN_YANG[dayGan];
  const otherYy = GAN_YIN_YANG[otherGan];
  const sameYy = dayYy === otherYy;

  if (dayWx === otherWx) return sameYy ? '比肩' : '劫财';
  if (WUXING_SHENG[otherWx] === dayWx) return sameYy ? '偏印' : '正印';
  if (WUXING_SHENG[dayWx] === otherWx) return sameYy ? '食神' : '伤官';
  if (WUXING_KE[otherWx] === dayWx) return sameYy ? '七杀' : '正官';
  if (WUXING_KE[dayWx] === otherWx) return sameYy ? '偏财' : '正财';
  return '';
}

